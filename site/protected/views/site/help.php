    <h1 style="text-align: center; "><strong>Как забронировать номер в гостинице на сайте Hotels24.ua</strong></h1>

    <p>&nbsp;</p>

    <p style="margin-left: 40px; ">Чтобы забронировать номер в одной из <a href="http://hotels24.ua/all_cities/">2330
            гостиниц</a> представленных на нашем сайте, пожалуйста, выполните шаги описанные ниже:</p>

    <p style="margin-left: 40px; ">&nbsp;</p>

    <p style="margin-left: 80px; "><strong><span style="font-size: medium; ">Шаг 1. Выберите гостиницу</span></strong>
    </p>

    <p style="margin-left: 80px; ">&nbsp;</p>

    <p style="margin-left: 40px; ">1. Введите название города (или гостиницы), а также даты поездки в форме поиска на
        странице <a href="http://hotels24.ua/">http://hotels24.ua/</a> и нажмите на кнопку “Поиск”;</p>

    <p style="margin-left: 40px; ">2. В следующем окне Вы увидите список
        всех гостиниц города. Чтобы быстро найти подходящий вариант, выберите в
        колонке слева ценовой диапазон, количество звезд и требуемые услуги.
        Также Вы можете отсортировать гостиницы по цене, отзывам наших
        пользователей или рекомендациям Hotels24.ua;</p>

    <p style="margin-left: 40px; ">3. Выбрав одну из гостиниц в списке,
        ознакомьтесь с фото номеров, представленных на сайте, отзывами людей
        проживавших в ней, полным перечнем предоставляемых услуг, адресом и
        другой важной информацией;</p>

    <p style="margin-left: 40px; ">4. Выберите номер, который Вас заинтересовал и нажмите на кнопку “Забронировать”.</p>

    <p style="margin-left: 40px; ">&nbsp;</p>

    <p style="margin-left: 80px; "><strong><span style="font-size: medium; ">Шаг 2. Укажите свои данные для оформления бронирования</span></strong>
    </p>

    <p style="margin-left: 80px; ">&nbsp;</p>

    <p style="margin-left: 40px; ">После того, как Вы нажали кнопку
        "Забронировать", Вы увидите несколько полей, которые необходимо
        заполнить: Имя, Фамилия, номер телефона и Email.<br>
        Ваша заявка моментально поступит на обработку в наш круглосуточный отдел бронирования.</p>

    <p style="margin-left: 40px; "><strong>Обратите внимание, мы уважаем
            Вашу конфиденциальность и ни в коем случае не будем предоставлять Ваши
            персональные данные третьим сторонам без Вашего согласия. Эти данные
            необходимы исключительно для оформления бронирования.</strong></p>

    <p style="margin-left: 40px; ">&nbsp;</p>

    <p style="margin-left: 80px; "><strong><span style="font-size: medium; ">Шаг 3. Ожидайте звонка от нас для уточнения деталей</span></strong>
    </p>

    <p style="margin-left: 80px; ">&nbsp;</p>

    <p style="margin-left: 40px; ">В течении 20 минут после получения заявки
